package chapter21_mini_project.model;

public class MarketBookData {
	private String bisbn;
	private String btitle;
	private int bprice;
	private String bauthor;
	private String bsubname;
	private String bdivision;
	private String bdate;
	
	
	public String getBisbn() {
		return bisbn;
	}
	public void setBisbn(String bisbn) {
		this.bisbn = bisbn;
	}
	public String getBtitle() {
		return btitle;
	}
	public void setBtitle(String btitle) {
		this.btitle = btitle;
	}
	public int getBprice() {
		return bprice;
	}
	public void setBprice(int bprice) {
		this.bprice = bprice;
	}
	public String getBauthor() {
		return bauthor;
	}
	public void setBauthor(String bauthor) {
		this.bauthor = bauthor;
	}
	public String getBsubname() {
		return bsubname;
	}
	public void setBsubname(String bsubname) {
		this.bsubname = bsubname;
	}
	public String getBdivision() {
		return bdivision;
	}
	public void setBdivision(String bdivision) {
		this.bdivision = bdivision;
	}
	public String getBdate() {
		return bdate;
	}
	public void setBdate(String bdate) {
		this.bdate = bdate;
	}
}
